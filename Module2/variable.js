// variables are containers for storing data

// var,let,const
// var - we can redeclare and reassign values
var a;
a = 10;
console.log(a);

// let - we can only reassign value
let b;
b = 20;
console.log(b);

// const -> reassigning and redeclare cannot be done
// we must assign and declare variables at single time
const pi = 3.1457;
console.log(pi);
